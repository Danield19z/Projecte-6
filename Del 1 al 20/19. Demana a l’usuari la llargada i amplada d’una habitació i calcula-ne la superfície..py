# Demanar la llargada i amplada de l'habitació
llargada = float(input("Introdueix la llargada de l'habitació en metres: "))
amplada = float(input("Introdueix l'amplada de l'habitació en metres: "))

# Calcular la superfície
superficie = llargada * amplada

# Mostrar el resultat
print(f"La superfície de l'habitació és: {superficie:.2f} m²")
