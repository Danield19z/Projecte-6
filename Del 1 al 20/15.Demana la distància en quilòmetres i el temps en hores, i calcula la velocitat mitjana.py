# Demanar la distància en quilòmetres
distancia = float(input("Introdueix la distància en km: "))

# Demanar el temps en hores
temps = float(input("Introdueix el temps en hores: "))

# Calcular la velocitat mitjana
velocitat_mitjana = distancia / temps

# Mostrar el resultat
print(f"La velocitat mitjana és {velocitat_mitjana:.2f} km/h")
